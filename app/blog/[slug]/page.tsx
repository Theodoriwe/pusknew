// ─── Helpers ──────────────────────────────────────────────────────────────────

export function getArticle(slug: string): Article | null {
  return articlesData[slug] ?? null;
}

/** Count words in all text blocks and estimate read time */
function calcReadTime(content: ArticleBlock[]): number {
  const words = content.reduce((acc, block) => {
    if (Array.isArray(block.text)) return acc + block.text.join(" ").split(" ").length;
    return acc + (block.text as string).split(" ").length;
  }, 0);
  return Math.ceil(words / 200); // 200 wpm
}

// ─── Static generation ────────────────────────────────────────────────────────

export async function generateStaticParams() {
  return Object.keys(articlesData).map((slug) => ({ slug }));
}

// ─── Metadata (SEO) ───────────────────────────────────────────────────────────

export async function generateMetadata({
  params,
}: {
  params: Promise<{ slug: string }>;
}): Promise<Metadata> {
  const { slug } = await params;
  const article = getArticle(slug);

  if (!article) {
    return { title: "Статья не найдена" };
  }

  const siteUrl = process.env.NEXT_PUBLIC_SITE_URL ?? "https://example.com";
  const canonicalUrl = `${siteUrl}/blog/${slug}`;

  return {
    title: `${article.title} | Блог`,
    description: article.excerpt,

    // Canonical URL — prevents duplicate content issues
    alternates: { canonical: canonicalUrl },

    // Open Graph — controls how the page looks when shared
    openGraph: {
      title: article.title,
      description: article.excerpt,
      url: canonicalUrl,
      siteName: "Название сайта",
      type: "article",
      publishedTime: article.date,
      authors: [article.author.name],
      tags: [article.category],
      images: [
        {
          url: article.image,
          width: 1200,
          height: 630,
          alt: article.title,
        },
      ],
    },

    // Twitter Card
    twitter: {
      card: "summary_large_image",
      title: article.title,
      description: article.excerpt,
      images: [article.image],
    },

    // Robots
    robots: {
      index: true,
      follow: true,
      googleBot: { index: true, follow: true },
    },
  };
}

// ─── JSON-LD helper ───────────────────────────────────────────────────────────

function buildJsonLd(article: Article, slug: string) {
  const siteUrl = process.env.NEXT_PUBLIC_SITE_URL ?? "https://example.com";

  return {
    "@context": "https://schema.org",
    "@type": "Article",
    headline: article.title,
    description: article.excerpt,
    image: article.image,
    datePublished: article.date,
    dateModified: article.date,
    author: {
      "@type": "Person",
      name: article.author.name,
      jobTitle: article.author.role,
    },
    publisher: {
      "@type": "Organization",
      name: "Название компании",
      logo: {
        "@type": "ImageObject",
        url: `${siteUrl}/logo.png`,
      },
    },
    mainEntityOfPage: {
      "@type": "WebPage",
      "@id": `${siteUrl}/blog/${slug}`,
    },
    // Estimated read time in ISO 8601 duration format
    timeRequired: `PT${calcReadTime(article.content)}M`,
  };
}

